//Klass: En klass är en mall eller ritning för att skapa objekt. Den definierar egenskaper och metoder som objekten som skapas från klassen kommer att ha.

//Instans av en klass (objekt): En instans är ett konkret objekt skapat från en klass. Ett objekt är en unik instans av en klass med sina egna värden för de egenskaper klassen definierar.

//Egenskap (property) i en klass: En egenskap är en variabel som är definierad i klassen och används för att hålla värden som är unika för varje objekt.

//Metod i en klass: En metod är en funktion som definieras i en klass och kan användas av objekt som skapas från klassen. Metoder kan manipulera objektets egenskaper och utföra specifika uppgifter.

//Inkapsling: Inkapsling innebär att dölja en klass interna detaljer och endast tillhandahålla ett gränssnitt för att interagera med objektet. Det används för att skydda data och kontrollera åtkomst. Kodmässigt görs det genom att använda privata variabler/metoder (t.ex. private i C# eller _ i Python).

//Konstruktor: En konstruktor är en speciell metod som körs när en ny instans av en klass skapas. Den används för att initiera objektets egenskaper.

//Instansvariabler: Instansvariabler är variabler som tillhör en specifik instans av en klass. De definieras vanligtvis i konstruktorn och kan variera från objekt till objekt.

//Nyckelordet ”this”: Nyckelordet this används för att referera till den nuvarande instansen av klassen inuti klassens metoder.Det används ofta för att skilja på lokala och instansvariabler.

//Deklarera en collection av objekt: En samling objekt kan deklareras med hjälp av olika typer av samlingar som List, Array, eller Dictionary. Exempel i C#: List<MyClass> myObjects = new List<MyClass>();.

//Namespace: Ett namespace är en logisk grupp av klasser och andra typer som används för att organisera och undvika namnkollisioner.Det hjälper till att hantera stora kodbaser.

